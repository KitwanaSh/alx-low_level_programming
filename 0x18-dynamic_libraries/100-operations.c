#include <stdio.h>

/**
 * add - adds two numbers
 * @a: first number
 * @b: the second number
 * return: the sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first number
 * @b: the second number
 * return: the sub
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: the second number
 * return: the product of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: the second number
 * return: the div
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the module two numbers
 * @a: first number
 * @b: the second number
 * return: the module of a and b
 */

int mod(int a, int b)
{
	return (a % b);
}
